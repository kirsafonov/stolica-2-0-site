export function getCaseBySlug(slug: string) {
  return cases.find((item) => item.slug === slug);
}

export function getRelatedCases(currentCase: CaseItem) {
  const related = currentCase.relatedSlugs
    .map((slug) => getCaseBySlug(slug))
    .filter(Boolean) as CaseItem[];

  if (related.length > 0) {
    return related;
  }

  return cases.filter((item) => item.slug !== currentCase.slug).slice(0, 3);
}